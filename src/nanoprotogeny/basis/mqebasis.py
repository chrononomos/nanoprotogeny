# ── Derived sets (for routing logic in mqeprotogeny.py) ─────────────────────

def _shell_types(symbol: str) -> List[str]:
    return [s[0] for s in STO3G.get(symbol, [])]

#: Elements whose STO-3G basis contains at least one D shell.
HAS_D: frozenset = frozenset(
    sym for sym, shells in STO3G.items()
    if any(stype == "D" for stype, *_ in shells)
)

#: Elements whose STO-3G basis contains SP shells (but no D shell).
HAS_SP: frozenset = frozenset(
    sym for sym, shells in STO3G.items()
    if any(stype == "SP" for stype, *_ in shells) and sym not in HAS_D
)

#: All elements with only a single S shell (H, He, Li outer, etc.)
HAS_S_ONLY: frozenset = frozenset(
    sym for sym, shells in STO3G.items()
    if all(stype == "S" for stype, *_ in shells)
)


def get_shells(symbol: str) -> List[ShellEntry]:
    """Return the list of :class:`ShellEntry` for *symbol*.

    Falls back to a minimal 3-Gaussian s-only shell with exponents
    (1.0, 0.3, 0.1) if the element is not in the registry.
    """
    return STO3G.get(
        symbol,
        [("S", (1.0000000, 0.3000000, 0.1000000),
                (-0.1000000, 0.2000000, 0.9000000), None)],
    )


def n_contracted(symbol: str) -> int:
    """Number of contracted basis functions for *symbol*.

    Each S shell contributes 1, each SP shell contributes 4 (1s + 3p),
    each D shell contributes 5 (cartesian) or 1 is counted for use in
    the seed-free analytical path which treats D as a single radial.
    """
    count = 0
    for stype, *_ in get_shells(symbol):
        if stype == "S":
            count += 1
        elif stype in ("SP",):
            count += 4   # 1s + 3p
        elif stype == "P":
            count += 3
        elif stype == "D":
            count += 5   # 5 cartesian d functions
    return count
