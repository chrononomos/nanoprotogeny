r"""
mqebasis.py — Full STO-3G Basis Set (Python-Native Format)
==========================================================
Authoritative STO-3G data for all 53 elements supported by this library,
parsed directly from the EMSL Basis Set Exchange ``sto-3g.dat``
(Hehre, Stewart & Pople 1969; Pietro & Hehre 1983).

This module replaces the simplified per-element snippets formerly inlined
in ``mqeprotogeny.py``.  Using the same contracted Gaussian primitives
as PySCF ensures that MQE-native CAS integrals are directly comparable to
PySCF reference calculations.

Data Format
-----------
``STO3G`` maps element symbol → list of shell tuples::

    (angular_type, exponents, coeffs_1, coeffs_2)

where

``angular_type``
    One of ``"S"``, ``"SP"``, ``"P"``, ``"D"``.

``exponents``
    3-tuple of primitive Gaussian exponents [bohr⁻²].

``coeffs_1``
    3-tuple of contraction coefficients:
    * s-type coefficients for ``"S"`` and ``"SP"`` shells;
    * d-type coefficients for ``"D"`` shells.

``coeffs_2``
    3-tuple of p-type contraction coefficients for ``"SP"`` shells;
    ``None`` for ``"S"``, ``"D"`` and ``"P"`` shells.

Shell Counts (selected elements)
---------------------------------
Element  Contracted basis       Shells (types)
H        [1s]                   S
C        [2s,1p]                S, SP
N        [2s,1p]                S, SP
O        [2s,1p]                S, SP
S        [3s,2p]                S, SP, SP
Fe       [4s,3p,1d]             S, SP, SP, SP, D
Mo       [5s,4p,2d]             S, SP, SP, SP, SP, D, D
Ti/V/Mn/Cu  [4s,3p,1d]         S, SP, SP, SP, D

References
----------
W.J. Hehre, R.F. Stewart and J.A. Pople, J. Chem. Phys. 2657 (1969).
W.J. Hehre, R. Ditchfield, R.F. Stewart, J.A. Pople, J. Chem. Phys. 2769 (1970).
W.J. Pietro, B.A. Levy, W.J. Hehre, R.F. Stewart, J. Am. Chem. Soc. 19, 2225 (1980).
W.J. Pietro and W.J. Hehre, J. Comp. Chem. 4, 241 (1983).
Source file: EMSL Basis Set Exchange Library, 9/15/14.

Dependencies
------------
None.  Pure Python stdlib — no numpy, no pyscf, no scipy.
"""

